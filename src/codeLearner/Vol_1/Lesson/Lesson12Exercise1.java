package codeLearner.Vol_1.Lesson;

    public class Lesson12Exercise1 {
/*
* write a java program that declares three  variables named age1,age2,age3.
  Assign any age you wish to each of the three variables, then use the
  println() method to output the ages to the screen separated by commas.

  */
    public static void main(String[] args) {
        int age1;
        int age2;
        int age3;

        age1 = 10;
        age2 = 14;
        age3 = 36;

        System.out.println(age1 + ", " + age2 +", " + age3); // output 10, 14, 36


}

}

