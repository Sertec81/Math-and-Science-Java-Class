package codeLearner.Vol_1.Lesson;

public class lesson14Exercise2 {
    /*
    Exercise2

    Write a java program to convert 10.5 feet and convert it to meters.
    There are 0.305 meters per foot.
     */
    public static void main(String[] args) {
        double feet, meters;
        feet = 10.5;        // assign the feet
        meters = feet*0.305;  // calculation
        System.out.println(feet + " feet is equal to " + meters + " meters ");  //  output 10.5 feet is equal to 3.2025 meters


    }
}